public class MyVariable1 {
    public static void main (String[] args) {
        String name = "Olga"; //after a statement, don't forget the semi-colon
        int hours = 40; //we are assigning 40 to the hours variable
        System.out.println(name); //Please note, when we have a string value, it is nested in double quotes
        System.out.println(hours); // When we are referring to numbers/integers, no quotes
    }
}
